  private String fileName;
  private String fileID;
  private String file_format_version;
  private ArrayList<Slide_GWT> slides = new ArrayList<Slide_GWT>();

  int currentSlideIndex = 0;
  
  // ---------------------------------------------------------------------------
  // ---------------------------------------------------------------------------

  // Return false there is not next slide
  public boolean doNextSlide()
  {
    if(currentSlideIndex == (slides.size() - 1))
      return false;
    currentSlideIndex++;
    return true;
  }
  
  // ---------------------------------------------------------------------------
  
  public boolean doPrevSlide()
  {
    if(currentSlideIndex == 0)
      return false;
    currentSlideIndex--;
    return true;
  }
  
  // ---------------------------------------------------------------------------

  @Override
  public void draw(Context2d context)
  {
    Slide_GWT slide = slides.get(currentSlideIndex);
    for(WeSketchBaseEvent_GWT event : slide.getAllEvents())
    {
      slide.draw(context);
      event.draw(context);
    }
  }
  
  // ---------------------------------------------------------------------------
  
